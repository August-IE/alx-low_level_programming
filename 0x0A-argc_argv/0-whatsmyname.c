#include <stdio.h>

/**
 * main - A function that prints its name, followed by a new line.
 * @argc: The arguments count of the program.
 * @argv: An array of pointers to the arguments.
 *
 * Return: Always o.
 */
int main(int __attribute__((__unused__)) argc, char *argv[])
{
	printf("%s\n", argv[0]);

	return (0);
}
